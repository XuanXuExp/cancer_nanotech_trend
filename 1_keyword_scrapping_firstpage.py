links = firstPageExtract(tempo_soup)
for i in range(2, 126):
    browser.find_element_by_xpath('//*[@title="Next page of results"]').click()
    soup = BeautifulSoup(browser.page_source, "lxml")
    links += firstPageExtract(soup)

# get pubmed link csv file
with open('pub_links_cn.csv', "w+") as file:
    for item in links:
        file.write("{}\n".format(str(item)))
