def firstPageExtract(tempo_soup):
    publication_links = list()
    # Set waiting time to avoid high traffic
    browser.implicitly_wait(random.randint(2, 3))

    try:
        collect = tempo_soup.find_all('div', {'class': 'rprt'})
        for publication in collect:
            link = publication.find('div', {'class': 'rslt'}).find('a').get("href")
            publication_links.append(link)
    except:
        org = 'ORGANIZATION_NA'

    return publication_links

